from turtle import *
ht()
bgcolor('black')
c=['#f4c20d','#3cba54','#db3236']
speed(10)
clear()
penup()
goto(0,-240)
seth(60)
pendown()
for i in range(3):
    begin_fill()
    color(c[i])
    fd(120*1.73)
    circle(120,120)
    right(180)
    fd(120*1.73)
    right(60)
    circle(-240,120)
    end_fill()
    pencolor(c[(i+1)%3])
    circle(-240,120)
    right(120)

#center circle()
pu()
goto(0,-120)
pd()
seth(0)
pencolor('white')
pensize(10)
begin_fill()
fillcolor('#4885ed')
circle(120)
end_fill()
